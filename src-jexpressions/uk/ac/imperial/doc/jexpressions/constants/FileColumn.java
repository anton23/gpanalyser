package uk.ac.imperial.doc.jexpressions.constants;

public class FileColumn {
	private String file;
	private int column;
	
	public FileColumn(String file, int column) {
		super();
		this.file = file;
		this.column = column;
	}

	public String getFile() {
		return file;
	}

	public int getColumn() {
		return column;
	}
	
	
	
}
